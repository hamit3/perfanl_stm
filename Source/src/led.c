/*******************************************************************************
 *  function :    led_init
 ******************************************************************************/
void led_init(void) {

	/* Set port parameters */			//
	struct GPIO_Parameters GPIO;	//
	GPIO.Pin	= 15;								//
	GPIO.Mode = Output;						// 
	GPIO.OType = Push_Pull;				//
	GPIO.PuPd = No_PuPd;					//
	GPIO.Speed = Medium_Speed;		//
	
	/* Initialize button GPIOA 15*/
	GPIO_Init(GPIOA,GPIO);

/* Initialize button GPIOB 0 */	
	GPIO.Pin	= 0;
	GPIO_Init(GPIOB,GPIO);

/* Initialize button GPIOB 1 */	
	GPIO.Pin	= 1;
	GPIO_Init(GPIOB,GPIO);
											 
/* Initialize button GPIOB 2 */	
	GPIO.Pin	= 2;
	GPIO_Init(GPIOB,GPIO);

/* Initialize button GPIOB 3 */	
	GPIO.Pin	= 3;
	GPIO_Init(GPIOB,GPIO);
	
	/* Initialize button GPIOB 4 */	
	GPIO.Pin	= 4;
	GPIO_Init(GPIOB,GPIO);
}



/*******************************************************************************
 *  function :    led_set
 ******************************************************************************/
